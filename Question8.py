# # Q8. Discuss the limitations of regularized linear models and explain why they may not always be the best
# # choice for regression analysis.

# Regularized linear models, such as Ridge and Lasso regression, are powerful tools for regression analysis, but they do have limitations license

# Regularized linear models assume a linear relationship between predictors and the target variable. If the true relationship is highly nonlinear, these models may not capture it effectively.

# Regularized models can result in a loss of information when they eliminate certain predictors through feature selection.

# Regularized linear models are sensitive to the scale of predictors. Scaling is often necessary for these models to perform well.
    
# Solving the optimization problem for regularized models can be computationally expensive, especially for high-dimensional data.