# Q1. Explain the concept of R-squared in linear regression models. How is it calculated, and what does it
# represent?

# Answer - 

# R-squared in linear regression model is use to determine the accuracy of the model. it will be always increases as the independent features
# increase. it ranges from 0 to 1 and it is calculated by :

# R_square = 1 - (some of square residual / sum of square total)

# R square is decreases as soon as there is less independent features.