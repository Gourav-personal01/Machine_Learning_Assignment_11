# Q4. What are RMSE, MSE, and MAE in the context of regression analysis? How are these metrics
# calculated, and what do they represent?

# Answer - 

# MSE(Mean Squared error) - MSE is calculated by taking the average of the squared differences between the predicted values (ŷ) and the actual values (y) for all data points

# it is calculated by :

# Formula: MSE = (1/n) Σ(y - ŷ)²

# Root Mean Squared Error (RMSE) - RMSE is the square root of the MSE and is computed as the square root of the average of the squared differences between predicted and actual values.

# it is calculated by :

# Formula: RMSE = √(MSE)

# Mean Absolute Error (MAE): MAE is calculated by taking the average of the absolute differences between predicted values (ŷ) and actual values (y) for all data points.

# Formula: MAE = (1/n) Σ|y - ŷ|

# All three metrics are measures of prediction error, and lower values indicate better model performance.